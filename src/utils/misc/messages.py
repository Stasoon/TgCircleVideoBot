def get_convertation_error_or_none(duration, size, x, y) -> None | str:
    is_ok = True
    err_text = '–ö —Å–æ–∂–∞–ª–µ–Ω–∏—é, –Ø –Ω–µ –º–æ–≥—É —Å–∫–æ–Ω–≤–µ—Ä—Ç–∏—Ä–æ–≤–∞—Ç—å —ç—Ç–æ –≤–∏–¥–µ–æ. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –∏—Å–ø–æ–ª—å–∑—É–π –≤–∏–¥–µ–æ—Ä–µ–¥–∞–∫—Ç–æ—Ä —á—Ç–æ–±—ã' \
               ' –∏—Å–ø—Ä–∞–≤–∏—Ç—å –ø—Ä–æ–±–ª–µ–º—ã –Ω–∏–∂–µ: \n\n'

    # –ø—Ä–æ–≤–µ—Ä–∫–∞ –ø—Ä–æ–¥–æ–ª–∂–∏—Ç–µ–ª—å–Ω–æ—Å—Ç–∏
    if duration <= max_video_duration_sec:
        err_text += f'‚úÖ –í–∏–¥–µ–æ –Ω–µ –¥–æ–ª—å—à–µ {max_video_duration_sec} —Å–µ–∫—É–Ω–¥. \n'
    else:
        is_ok = False
        err_text += f'‚õî –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è –ø—Ä–æ–¥–æ–ª–∂–∏—Ç–µ–ª—å–Ω–æ—Å—Ç—å –≤–∏–¥–µ–æ - {max_video_duration_sec} —Å–µ–∫—É–Ω–¥. –í–∞—à–µ –≤–∏–¥–µ–æ –¥–ª–∏—Ç—Å—è <b>{duration}</b> —Å. \n'

    # –ø—Ä–æ–≤–µ—Ä–∫–∞ —Ä–∞–∑–º–µ—Ä–∞ (–±–∞–π—Ç—ã)
    if size <= 8*2**20: err_text += f'‚úÖ –í–∏–¥–µ–æ –º–µ–Ω—å—à–µ, —á–µ–º {max_video_size_mb} –ú–±. \n'
    else:
        is_ok = False
        err_text += f'‚õî –í–∏–¥–µ–æ –±–æ–ª—å—à–µ {max_video_size_mb} –ú–±. –†–∞–∑–º–µ—Ä –≤–∞—à–µ–≥–æ –≤–∏–¥–µ–æ - <b>{size / 2 ** 20 : .2f}</b> –ú–± \n'

    # –ø—Ä–æ–≤–µ—Ä–∫–∞ –æ—Ç–Ω–æ—à–µ–Ω–∏—è —Å—Ç–æ—Ä–æ–Ω
    if x == y: err_text += '‚úÖ –í–∏–¥–µ–æ –∏–º–µ–µ—Ç –æ—Ç–Ω–æ—à–µ–Ω–∏–µ —Å—Ç–æ—Ä–æ–Ω 1:1. \n'
    else:
        is_ok = False
        err_text += '‚õî –í–∏–¥–µ–æ –Ω–µ –∫–≤–∞–¥—Ä–∞—Ç–Ω–æ–µ, —à–∏—Ä–∏–Ω–∞ –∏ –≤—ã—Å–æ—Ç–∞ –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å –æ–¥–∏–Ω–∞–∫–æ–≤—ã (–æ—Ç–Ω–æ—à–µ–Ω–∏–µ —Å—Ç–æ—Ä–æ–Ω 1:1). ' + \
                    f'–†–∞–∑–º–µ—Ä—ã –≤–∞—à–µ–≥–æ –≤–∏–¥–µ–æ - <b>{x}x{y}</b> (–®x–í) \n'

    # –ø—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ —Ä–∞–∑–º–µ—Ä—ã —Å—Ç–æ—Ä–æ–Ω
    if x <= max_sides_len_px and y <= max_sides_len_px:
        err_text += f'‚úÖ –®–∏—Ä–∏–Ω–∞ –∏–ª–∏ –≤—ã—Å–æ—Ç–∞ –≤–∏–¥–µ–æ –Ω–µ –±–æ–ª—å—à–µ, —á–µ–º {max_sides_len_px} –ø–∏–∫—Å–µ–ª–µ–π. \n'
    else:
        is_ok = False
        err_text += f'‚õî –®–∏—Ä–∏–Ω–∞ –∏–ª–∏ –≤—ã—Å–æ—Ç–∞ –≤–∏–¥–µ–æ –±–æ–ª—å—à–µ —á–µ–º {max_sides_len_px}. –†–∞–∑–º–µ—Ä—ã –≤–∞—à–µ–≥–æ –≤–∏–¥–µ–æ - <b>{x}x{y}</b> (–®x–í) \n'

    return None if is_ok else err_text


